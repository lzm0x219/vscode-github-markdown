import githubMarkdownCss, { type GithubMarkdownCssTheme } from "generate-github-markdown-css";

export const githubThemes = [
  "light",
  "light_colorblind",
  "light_high_contrast",
  "light_tritanopia",
  "dark",
  "dark_colorblind",
  "dark_dimmed",
  "dark_high_contrast",
  "dark_tritanopia"
] as const satisfies readonly GithubMarkdownCssTheme[];

export type GithubTheme = (typeof githubThemes)[number];

export type CssAsset = {
  fileName: string;
  content: string;
};

const markdownRoot = ".vscode-github-markdown";
const sharedCssFile = "github-markdown.css";

export function githubCssFileNames(): string[] {
  return [sharedCssFile, ...githubThemes.map(themeFileName)];
}

export function githubCssImports(): string {
  return githubCssFileNames()
    .map((fileName) => `@import "./${fileName}";`)
    .join("\n");
}

export async function createGithubCssAssets(): Promise<CssAsset[]> {
  const [markdownStyles, ...themeResults] = await Promise.all([
    githubMarkdownCss({ onlyStyles: true, rootSelector: markdownRoot }),
    ...githubThemes.map(generateThemeVariables)
  ]);
  if (markdownStyles.includes("[data-theme=")) {
    throw new Error("Generated GitHub Markdown styles unexpectedly contain theme selectors");
  }

  const sharedVariables = themeResults[0]?.sharedVariables;
  if (!sharedVariables) throw new Error("Generated GitHub CSS has no shared variables");
  for (const result of themeResults) {
    if (result.sharedVariables !== sharedVariables) {
      throw new Error(`Generated shared variables differ for theme ${result.theme}`);
    }
  }

  return [
    {
      fileName: sharedCssFile,
      content: generatedCss(`${sharedVariables}\n\n${markdownStyles}`)
    },
    ...themeResults.map(({ theme, themeVariables }) => ({
      fileName: themeFileName(theme),
      content: generatedCss(renderThemeCss(theme, themeVariables))
    }))
  ];
}

export function splitThemeCss(
  css: string,
  theme: GithubTheme
): { sharedVariables: string; themeVariables: string } {
  const selector = `${markdownRoot},\n[data-theme="${theme}"] {\n`;
  const selectorStart = css.indexOf(selector);
  const blockEnd = css.lastIndexOf("\n}");
  if (selectorStart < 0 || blockEnd <= selectorStart) {
    throw new Error(`Cannot split generated CSS variables for theme ${theme}`);
  }
  return {
    sharedVariables: css.slice(0, selectorStart).trim(),
    themeVariables: css.slice(selectorStart + selector.length, blockEnd)
  };
}

export function renderThemeCss(theme: GithubTheme, variables: string): string {
  return [
    `${markdownRoot}[data-color-mode="light"][data-light-theme="${theme}"],
${markdownRoot}[data-color-mode="dark"][data-dark-theme="${theme}"] {
${variables}
}`,
    `@media (prefers-color-scheme: light) {
  ${markdownRoot}[data-color-mode="auto"][data-light-theme="${theme}"] {
${indent(variables)}
  }
}`,
    `@media (prefers-color-scheme: dark) {
  ${markdownRoot}[data-color-mode="auto"][data-dark-theme="${theme}"] {
${indent(variables)}
  }
}`
  ].join("\n\n");
}

async function generateThemeVariables(theme: GithubTheme) {
  const css = await githubMarkdownCss({
    light: theme,
    dark: theme,
    onlyVariables: true,
    rootSelector: markdownRoot
  });
  return { theme, ...splitThemeCss(css, theme) };
}

function themeFileName(theme: GithubTheme): string {
  return `github-markdown-theme-${theme}.css`;
}

function indent(value: string): string {
  return value
    .split("\n")
    .map((line) => `  ${line}`)
    .join("\n");
}

function generatedCss(css: string): string {
  return `/* Generated by \`nub run build\` or \`nub run dev\`. Do not edit. */\n\n${css}\n`;
}
