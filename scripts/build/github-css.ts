import githubMarkdownCss, {
  type GithubMarkdownCssOptions,
  type GithubMarkdownCssTheme
} from "generate-github-markdown-css";
import { readFile } from "node:fs/promises";
import { project } from "../shared/project";

export const githubThemes = [
  "light",
  "light_colorblind",
  "light_high_contrast",
  "light_tritanopia",
  "dark",
  "dark_colorblind",
  "dark_dimmed",
  "dark_high_contrast",
  "dark_tritanopia"
] as const satisfies readonly GithubMarkdownCssTheme[];

export type GithubTheme = (typeof githubThemes)[number];

export type CssAsset = {
  fileName: string;
  content: string;
};

export type GithubCssGenerator = (options: GithubMarkdownCssOptions) => Promise<string>;

const markdownRoot = ".vscode-github-markdown";
const sharedCssFile = "github-markdown.css";
const generatedHeader = "/* Generated by `nub run build` or `nub run dev`. Do not edit. */";
export const parityOnlyCssMarker = "/* Parity-only GitHub page styles. */";

export function githubCssFileNames(): string[] {
  return [sharedCssFile, ...githubThemes.map(themeFileName)];
}

export function githubCssImports(): string {
  return githubCssFileNames()
    .map((fileName) => `@import "./${fileName}";`)
    .join("\n");
}

export async function readGithubCssAssets(): Promise<CssAsset[]> {
  return parseGithubCssAssets(await readFile(project.paths.parityReferenceCss, "utf8"));
}

export function parseGithubCssAssets(referenceCss: string): CssAsset[] {
  const marker = `\n${parityOnlyCssMarker}\n`;
  const markerIndex = referenceCss.indexOf(marker);
  if (markerIndex < 0) {
    throw new Error("GitHub parity reference has no parity-only CSS marker");
  }

  const sections = referenceCss.slice(0, markerIndex).split(generatedHeader);
  const prefix = sections.shift();
  const fileNames = githubCssFileNames();
  if (prefix?.trim() || sections.length !== fileNames.length) {
    throw new Error(
      `GitHub parity reference has ${sections.length} build assets; expected ${fileNames.length}`
    );
  }

  return fileNames.map((fileName, index) => ({
    fileName,
    content: `${generatedHeader}${sections[index] ?? ""}`.trimEnd() + "\n"
  }));
}

export async function generateGithubCssAssets(
  generator: GithubCssGenerator = githubMarkdownCss,
  githubToken: string | undefined = process.env["GITHUB_TOKEN"]
): Promise<CssAsset[]> {
  return withAuthenticatedGitHubApi(githubToken, () => generateGithubCssAssetsWith(generator));
}

async function generateGithubCssAssetsWith(generator: GithubCssGenerator): Promise<CssAsset[]> {
  const markdownStyles = await generator({ onlyStyles: true, rootSelector: markdownRoot });
  const themeResults: Array<{
    theme: GithubTheme;
    sharedVariables: string;
    themeVariables: string;
  }> = [];
  for (const theme of githubThemes) {
    themeResults.push(await generateThemeVariables(theme, generator));
  }

  if (markdownStyles.includes("[data-theme=")) {
    throw new Error("Generated GitHub Markdown styles unexpectedly contain theme selectors");
  }

  const sharedVariables = themeResults[0]?.sharedVariables;
  if (!sharedVariables) throw new Error("Generated GitHub CSS has no shared variables");
  for (const result of themeResults) {
    if (result.sharedVariables !== sharedVariables) {
      throw new Error(`Generated shared variables differ for theme ${result.theme}`);
    }
  }

  return [
    {
      fileName: sharedCssFile,
      content: generatedCss(`${sharedVariables}\n\n${markdownStyles}`)
    },
    ...themeResults.map(({ theme, themeVariables }) => ({
      fileName: themeFileName(theme),
      content: generatedCss(renderThemeCss(theme, themeVariables))
    }))
  ];
}

async function withAuthenticatedGitHubApi<T>(
  token: string | undefined,
  operation: () => Promise<T>
): Promise<T> {
  if (!token) {
    return operation();
  }

  const originalFetch = globalThis.fetch;
  const authenticatedFetch: typeof fetch = (input, init) => {
    const requestUrl = new URL(input instanceof Request ? input.url : input);
    if (requestUrl.origin !== "https://api.github.com") {
      return originalFetch(input, init);
    }

    const headers = new Headers(input instanceof Request ? input.headers : undefined);
    new Headers(init?.headers).forEach((value, key) => headers.set(key, value));
    if (!headers.has("Authorization")) {
      headers.set("Authorization", `Bearer ${token}`);
    }
    return originalFetch(input, { ...init, headers });
  };

  globalThis.fetch = authenticatedFetch;
  try {
    return await operation();
  } finally {
    globalThis.fetch = originalFetch;
  }
}

export function splitThemeCss(
  css: string,
  theme: GithubTheme
): { sharedVariables: string; themeVariables: string } {
  const selector = `${markdownRoot},\n[data-theme="${theme}"] {\n`;
  const selectorStart = css.indexOf(selector);
  const blockEnd = css.lastIndexOf("\n}");
  if (selectorStart < 0 || blockEnd <= selectorStart) {
    throw new Error(`Cannot split generated CSS variables for theme ${theme}`);
  }
  return {
    sharedVariables: css.slice(0, selectorStart).trim(),
    themeVariables: css.slice(selectorStart + selector.length, blockEnd)
  };
}

export function renderThemeCss(theme: GithubTheme, variables: string): string {
  return [
    `${markdownRoot}[data-color-mode="light"][data-light-theme="${theme}"],
${markdownRoot}[data-color-mode="dark"][data-dark-theme="${theme}"] {
${variables}
}`,
    `body.vscode-light ${markdownRoot}[data-color-mode="auto"][data-light-theme="${theme}"],
body.vscode-high-contrast-light ${markdownRoot}[data-color-mode="auto"][data-light-theme="${theme}"] {
${indent(variables)}
}`,
    `body.vscode-dark ${markdownRoot}[data-color-mode="auto"][data-dark-theme="${theme}"],
body.vscode-high-contrast ${markdownRoot}[data-color-mode="auto"][data-dark-theme="${theme}"] {
${indent(variables)}
}`
  ].join("\n\n");
}

async function generateThemeVariables(theme: GithubTheme, generator: GithubCssGenerator) {
  const css = await generator({
    light: theme,
    dark: theme,
    onlyVariables: true,
    useFixture: false,
    rootSelector: markdownRoot
  });
  return { theme, ...splitThemeCss(css, theme) };
}

function themeFileName(theme: GithubTheme): string {
  return `github-markdown-theme-${theme}.css`;
}

function indent(value: string): string {
  return value
    .split("\n")
    .map((line) => `  ${line}`)
    .join("\n");
}

function generatedCss(css: string): string {
  return `${generatedHeader}\n\n${css}\n`;
}
